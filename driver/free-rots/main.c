// 简单的栈监控任务：周期性打印每个任务的高水位（单位：栈字）
void Task_StackMonitor(void *pvParameters) {
    char buf[128];
    for (;;) {
        UBaseType_t motor = uxTaskGetStackHighWaterMark(TaskHandle_Motor);
        UBaseType_t lidar = uxTaskGetStackHighWaterMark(TaskHandle_Lidar);
        UBaseType_t can = uxTaskGetStackHighWaterMark(TaskHandle_CAN);
        UBaseType_t self = uxTaskGetStackHighWaterMark(NULL);
        int len = snprintf(buf, sizeof(buf), "StackHighWater (words): Motor=%lu Lidar=%lu CAN=%lu Self=%lu\r\n",
                           (unsigned long)motor, (unsigned long)lidar, (unsigned long)can, (unsigned long)self);
        HAL_UART_Transmit(&huart1, (uint8_t*)buf, (uint16_t)len, HAL_MAX_DELAY);
        vTaskDelay(pdMS_TO_TICKS(1000));
    }
}