// ==========================================================
// 主函数 (Entry Point)
// ==========================================================
int main(void) {
    // 1. 复位所有外设，初始化 Flash 接口和 Systick
    HAL_Init();

    // 2. 配置系统时钟 (配置 RCC_CFGR 等，建立系统心跳)
    SystemClock_Config();

    // 3. 初始化所有底层硬件
    MX_GPIO_Init();
    MX_USART1_UART_Init();
    MX_I2C1_Init();
    MX_CAN1_Init();

    // 4. 创建 RTOS 资源
    xI2C_Mutex = xSemaphoreCreateMutex();
    if (xI2C_Mutex == NULL) {
        // 内存不足，创建失败，死机保护
        while(1); 
    }
    // 使用由 FreeRTOS 提供的互斥量 (mutex) 而不是二值信号量，
    // 以启用优先级继承 (Priority Inheritance)，缓解优先级翻转问题。

    // 5. 创建系统任务 (注意分配实际的堆栈大小，单位是 Word)
    xTaskCreate(Task_MotorControl, "Motor", 256, NULL, 5, &TaskHandle_Motor);
    xTaskCreate(Task_LidarParse,  "Lidar", 512, NULL, 3, &TaskHandle_Lidar);
    xTaskCreate(Task_CANOpen,     "CAN",   256, NULL, 1, &TaskHandle_CAN);
    // 任务用于监控每个任务的栈高水位（Stack High Water Mark）
    xTaskCreate(Task_StackMonitor, "StackMon", 256, NULL, 2, NULL);

    // 6. 启动调度器，接管 CPU
    vTaskStartScheduler();

    // 永远不应该执行到这里，除非 FreeRTOS 堆内存耗尽
    while (1) {
    }
}

// ==========================================================
// 4. 任务实体具体实现
// ==========================================================

// [最高优先级] 电机与运动学解算任务
void Task_MotorControl(void *pvParameters) {
    uint8_t imu_data[6];
    
    for(;;) {
        // 尝试获取 I2C 总线锁，等待 10 个 RTOS Tick
        if (xSemaphoreTake(xI2C_Mutex, pdMS_TO_TICKS(10)) == pdTRUE) {
            
            // 成功获取锁，调用 HAL 库读取 MPU6050/BMI088
            HAL_I2C_Mem_Read(&hi2c1, 0xD0, 0x3B, I2C_MEMADD_SIZE_8BIT, imu_data, 6, HAL_MAX_DELAY);
            
            // 释放锁，允许其他任务使用 I2C
            xSemaphoreGive(xI2C_Mutex);
            
            // TODO: 执行 FOC 算法或 PID 计算，并通过 PWM 输出
            
        } else {
            // 锁获取失败，触发总线复位或安全停机逻辑
        }
        
        // 绝对延时，保证控制环路严格的 2ms (500Hz) 运行频率
        vTaskDelay(pdMS_TO_TICKS(2)); 
    }
}

